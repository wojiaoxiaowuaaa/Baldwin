"""
变量可以存储不同类型的数据，不同类型可以执行不同的操作。

文本类型：str

数值类型：int、float、complex

序列类型：list、tuple、range

映射类型：dict

集合类型：set、frozenset

布尔类型：bool

二进制类型：bytes、bytearray

空类型：NoneType
"""

x = "Hello World"

x = 20

x = 20.5  # float

x = ["apple", "banana", "cherry"]

x = ("apple", "banana", "cherry")

x = range(6)  # range

x = {"name": "John", "age": 36}

x = {"apple", "banana", "cherry"}  # set

x = frozenset({"apple", "banana", "cherry"})  # frozenset

x = True  # bool

x = b"Hello"  # bytes

x = None  # NoneType
